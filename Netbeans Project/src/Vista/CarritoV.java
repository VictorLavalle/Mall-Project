    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
          int index= this.TablaPaquetes.getSelectedRow();
       
        if(index!=-1){
        int counter=0;
        Iterator i = this.cliente.getCarrito().getPaq();
        
        while(i.hasNext()&&  counter<= index){
        
            Paquete paq= (Paquete) i.next();
            
            if(counter==index){
                this.cliente.getCarrito().QuitarPaquete(paq);
            }
            counter++;
        
        }
        
       LlenarPaquetes();
        calcularmonto();
        
        
        }
    }//GEN-LAST:event_jButton3ActionPerformed

