     public void llenarArticulos() {
        String tabla[][] = new String[this.cliente.getCarrito().sizeArt()][4];
        Iterator articulosIterator = this.cliente.getCarrito().getArticulos();
        int i = 0;
        while (articulosIterator.hasNext()) {
            Articulo articulo = (Articulo) articulosIterator.next();
            tabla[i][0] = articulo.getNombre();
            tabla[i][1] = articulo.getID();
            tabla[i][2] = articulo.getIDT();
            tabla[i][3] = "$" + String.format("%.2f", articulo.getPrecio());
            i++;
        }

        TablaArticulos.setModel(new javax.swing.table.DefaultTableModel(
                tabla,
                new String[]{
                    "Articulo","ID","Tienda", "Precio"}
        ));
    }
       
       
         public void LlenarPaquetes() {
       String tabla[][] = new String[this.cliente.getCarrito().sizePaq()][3];
        Iterator paquetesIterator = this.cliente.getCarrito().getPaq();
        int i = 0;
        while (paquetesIterator.hasNext()) {
            Paquete paquete = (Paquete) paquetesIterator.next();
            tabla[i][0] = paquete.getNombre();
            tabla[i][1] = "$" + String.format("%.2f", paquete.getPrecio());
            i++;
        }

        TablaPaquetes.setModel(new javax.swing.table.DefaultTableModel(
                tabla,
                new String[]{
                    "Paquete", "Precio"}
        ));
    }

    
    public void calcularmonto(){
    int monto=0;
      Iterator i = this.cliente.getCarrito().getArticulos();
      Iterator j = this.cliente.getCarrito().getPaq();
      
      while(i.hasNext()){
      
          Articulo art= (Articulo) i.next();
          
          monto+=art.getPrecio();
      
      }
      
      while (j.hasNext()){
      
          Paquete paq = (Paquete) j.next();
      
          monto+=paq.getPrecio();
      
      }
     
      this.Monto.setText(" $"+monto);
      
    
    }
    
    
